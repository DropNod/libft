#include "libft.h"

void	*ft_memset(void *ptr, int value, size_t size)
{
	size_t			i;
	unsigned char	*u_ptr;

	i = -1;
	u_ptr = (unsigned char *) ptr;
	while (++i < size)
		u_ptr[i] = (unsigned char) value;
	return (ptr);
}
