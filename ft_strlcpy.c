#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	i;

	i = -1;
	while (src[++i] && i + 1 < size)
		dest[i] = src[i];
	if (size > 0)
		dest[i] = '\0';
	return (ft_strlen(src));
}
